/**
 * Picks a random template for the given relation type and
 * replaces {name} with the recipient's name.
 */
export function generateMessage(
  relationType: RelationType,
  recipientName: string
): string {
  const templates = MESSAGE_TEMPLATES[relationType];
  const idx = Math.floor(Math.random() * templates.length);
  return templates[idx].replace(/{name}/g, recipientName);
}

/**
 * Returns the total number of templates available for a relation type.
 */
export function getTemplateCount(relationType: RelationType): number {
  return MESSAGE_TEMPLATES[relationType].length;
}

/**
 * Picks a specific template by index (wraps around).
 */
export function getTemplateByIndex(
  relationType: RelationType,
  index: number,
  recipientName: string
): string {
  const templates = MESSAGE_TEMPLATES[relationType];
  const idx = ((index % templates.length) + templates.length) % templates.length;
  return templates[idx].replace(/{name}/g, recipientName);
}
